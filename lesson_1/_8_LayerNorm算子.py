'''LayerNorm层归一化内部实现讲解'''
# LayerNorm（Layer Normalization，层归一化）
# 是深度学习中常用的归一化技术，用于稳定神经网络的训练过程
# 作用：层归一化，稳定大模型训练。
# 1. 输入形状：假设输入是一个形状为 (batch_size, feature_size) 的张量
# 2. 计算均值和方差：对每个样本独立计算均值和方差（沿特征维度）
    # μ = (1/N) Σ xᵢ
    # σ² = (1/N) Σ (xᵢ - μ)²
# 3. 归一化：使用计算得到的均值和方差对特征进行归一化
    # x̂ = (x - μ) / √(σ² + ε) # ε 是一个很小的数（1e-5，或1e-8），防止除以零
# 4. 缩放和平移：应用可学习的参数 伽马γ (缩放) 和 贝塔β (平移)：y = γ * x̂ + β
import torch
ln = torch.nn.LayerNorm(3) # 创建一个 LayerNorm 层，特征维度为 3
# 随机生成输入数据 (batch_size=2, feature_size=3)，2个样本，3个特征
x = torch.randn(2, 3) # 假如输出tensor([[-0.5,  1.0,  0.3],[ 0.8, -0.2,  1.5]]）
print(x)
# 1. 初始化参数
# LayerNorm 会自动初始化两个可学习参数：
    # γ (缩放参数): 初始化为全1向量 [1, 1, 1]
    # β (平移参数): 初始化为全0向量 [0, 0, 0]
# 2. 对第一个样本 [-0.5, 1.0, 0.3] 的计算
    # 步骤1: 计算均值 μ = (-0.5 + 1.0 + 0.3) / 3 = 0.8 / 3 ≈ 0.2667
    # 步骤2: 计算方差(σ²)
    # σ² = [(-0.5-0.2667)² + (1.0-0.2667)² + (0.3-0.2667)²] / 3
    #    = [(-0.7667)² + (0.7333)² + (0.0333)²] / 3
    #    = [0.5878 + 0.5378 + 0.0011] / 3
    #    ≈ 1.1267 / 3 ≈ 0.3756
# 步骤3: 归一化
    # ε = 1e-5 (防止除以零的小常数)
    # x̂ = (x - μ) / √(σ² + ε)
    #
    # x̂₁ = (-0.5 - 0.2667) / √(0.3756 + 1e-5) ≈ -0.7667 / 0.6129 ≈ -1.2509
    # x̂₂ = (1.0 - 0.2667) / 0.6129 ≈ 1.1960
    # x̂₃ = (0.3 - 0.2667) / 0.6129 ≈ 0.0543
# 步骤4: 缩放和平移
# y = γ * x̂ + β
#
# y₁ = 1 * -1.2509 + 0 = -1.2509
# y₂ = 1 * 1.1960 + 0 = 1.1960
# y₃ = 1 * 0.0543 + 0 = 0.0543
# 3. 对第二个样本的计算同上，缩放和平移以后：
# y₁ = 1 * 0.1433 + 0 = 0.1433
# y₂ = 1 * -1.2907 + 0 = -1.2907
# y₃ = 1 * 1.1474 + 0 = 1.1474
# 应用 LayerNorm
out = ln(x)
# 最终输出
# tensor([[-1.2509,  1.1960,  0.0543],
#         [ 0.1433, -1.2907,  1.1474]])
print(out.shape)
print(out)
